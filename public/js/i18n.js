var _currentLang = 'pt-BR';

function __(key) {
  var langData = _lang[_currentLang] || _lang['pt-BR'];
  return langData[key] || key;
}

function setLanguage(lang) {
  if (!_lang[lang]) return;
  _currentLang = lang;
  try {
    localStorage.setItem('cyberpdv_lang', lang);
  } catch (_) {}
  document.documentElement.setAttribute('lang', lang === 'pt-BR' ? 'pt' : 'en');
  applyTranslations();
}

function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(function (el) {
    var key = el.getAttribute('data-i18n');
    el.textContent = __(key);
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(function (el) {
    var key = el.getAttribute('data-i18n-placeholder');
    el.placeholder = __(key);
  });
  document.querySelectorAll('[data-i18n-title]').forEach(function (el) {
    var key = el.getAttribute('data-i18n-title');
    el.title = __(key);
  });
  document.querySelectorAll('[data-i18n-value]').forEach(function (el) {
    var key = el.getAttribute('data-i18n-value');
    el.value = __(key);
  });
}

function loadLanguage() {
  try {
    var stored = localStorage.getItem('cyberpdv_lang');
    if (stored && _lang[stored]) {
      _currentLang = stored;
    }
  } catch (_) {}
  document.documentElement.setAttribute('lang', _currentLang === 'pt-BR' ? 'pt' : 'en');
}

loadLanguage();
